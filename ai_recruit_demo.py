def parse_ai_json(content):
    if not content:
        raise json.JSONDecodeError("empty content", "", 0)

    cleaned = content.replace("```json", "").replace("```", "").strip()
    try:
        return json.loads(cleaned)
    except json.JSONDecodeError:
        match = re.search(r"\{[\s\S]*\}", cleaned)
        if not match:
            raise
        return json.loads(match.group(0))


def read_uploaded_text(uploaded_file):
    suffix = Path(uploaded_file.name).suffix.lower()
    data = uploaded_file.getvalue()

    if suffix in {".txt", ".md", ".csv", ".json"}:
        for encoding in ("utf-8", "utf-8-sig", "gbk"):
            try:
                return data.decode(encoding)
            except UnicodeDecodeError:
                continue
        return data.decode("utf-8", errors="ignore")

    if suffix == ".pdf":
        try:
            from pypdf import PdfReader
        except ImportError as exc:
            raise RuntimeError("缺少 pypdf，请先安装依赖") from exc

        with tempfile.NamedTemporaryFile(delete=False, suffix=suffix) as tmp_file:
            tmp_file.write(data)
            tmp_path = Path(tmp_file.name)

        try:
            reader = PdfReader(str(tmp_path))
            return "\n".join(page.extract_text() or "" for page in reader.pages)
        finally:
            tmp_path.unlink(missing_ok=True)

    if suffix == ".docx":
        try:
            from docx import Document
        except ImportError as exc:
            raise RuntimeError("缺少 python-docx，请先安装依赖") from exc

        with tempfile.NamedTemporaryFile(delete=False, suffix=suffix) as tmp_file:
            tmp_file.write(data)
            tmp_path = Path(tmp_file.name)

        try:
            document = Document(str(tmp_path))
            return "\n".join(paragraph.text for paragraph in document.paragraphs)
        finally:
            tmp_path.unlink(missing_ok=True)

    raise RuntimeError(f"不支持的简历格式：{suffix or '未知'}")


