def extract_audio_with_ffmpeg(video_path):
    if not shutil.which("ffmpeg"):
        return None

    audio_handle = tempfile.NamedTemporaryFile(delete=False, suffix=".wav")
    audio_handle.close()
    audio_path = Path(audio_handle.name)

    command = [
        "ffmpeg",
        "-y",
        "-i",
        str(video_path),
        "-vn",
        "-ac",
        "1",
        "-ar",
        "16000",
        str(audio_path),
    ]
    completed = subprocess.run(command, capture_output=True, text=True)
    if completed.returncode != 0 or not audio_path.exists():
        audio_path.unlink(missing_ok=True)
        return None

    return audio_path


def transcribe_with_sensevoice(video_path, model_name):
    audio_path = extract_audio_with_ffmpeg(video_path)
    if not audio_path:
        return ""

    try:
        from funasr import AutoModel
    except ImportError:
        audio_path.unlink(missing_ok=True)
        return ""

    try:
        asr_model = AutoModel(model=model_name, disable_update=True)
        output = asr_model.generate(input=str(audio_path))
    except Exception:
        audio_path.unlink(missing_ok=True)
        return ""
    finally:
        audio_path.unlink(missing_ok=True)

    transcript = ""
    if isinstance(output, list) and output:
        first_item = output[0]
        if isinstance(first_item, dict):
            transcript = first_item.get("text") or first_item.get("sentence") or first_item.get("preds") or ""
        else:
            transcript = str(first_item)
    elif isinstance(output, dict):
        transcript = output.get("text") or output.get("sentence") or output.get("preds") or ""
    else:
        transcript = str(output)

    return transcript.strip()


def summarize_face_mesh(video_path, sample_interval):
    try:
        import cv2
        import mediapipe as mp
    except ImportError:
        return "FaceMesh 不可用：缺少 mediapipe 或 opencv-python。"

    capture = cv2.VideoCapture(str(video_path))
    if not capture.isOpened():
        return "视频无法打开。"

    frame_count = 0
    sampled_count = 0
    face_count = 0
    center_motion = []
    mouth_motion = []
    eye_opening = []
    previous_center = None

    with mp.solutions.face_mesh.FaceMesh(
        static_image_mode=False,
        refine_landmarks=True,
        max_num_faces=1,
        min_detection_confidence=0.5,
        min_tracking_confidence=0.5,
    ) as face_mesh:
        while True:
            ok, frame = capture.read()
            if not ok:
                break

            if frame_count % sample_interval != 0:
                frame_count += 1
                continue

            sampled_count += 1
            rgb_frame = cv2.cvtColor(frame, cv2.COLOR_BGR2RGB)
            result = face_mesh.process(rgb_frame)
            if not result.multi_face_landmarks:
                frame_count += 1
                continue

            face_count += 1
            landmarks = result.multi_face_landmarks[0].landmark
            xs = [point.x for point in landmarks]
            ys = [point.y for point in landmarks]
            center = ((min(xs) + max(xs)) / 2, (min(ys) + max(ys)) / 2)

            if previous_center is not None:
                center_motion.append(abs(center[0] - previous_center[0]) + abs(center[1] - previous_center[1]))
            previous_center = center

            mouth_motion.append(abs(landmarks[13].y - landmarks[14].y))
            eye_opening.append(
                (
                    abs(landmarks[159].y - landmarks[145].y)
                    + abs(landmarks[386].y - landmarks[374].y)
                )
                / 2
            )

            frame_count += 1

    capture.release()

    if sampled_count == 0:
        return "未采样到有效视频帧。"

    detection_ratio = face_count / sampled_count
    avg_center_motion = sum(center_motion) / len(center_motion) if center_motion else 0.0
    avg_mouth_motion = sum(mouth_motion) / len(mouth_motion) if mouth_motion else 0.0
    avg_eye_opening = sum(eye_opening) / len(eye_opening) if eye_opening else 0.0

    return (
        f"FaceMesh统计：采样帧数={sampled_count}，有人脸帧数={face_count}，"
        f"人脸出现率={detection_ratio:.2f}，头部中心位移均值={avg_center_motion:.4f}，"
        f"嘴部变化均值={avg_mouth_motion:.4f}，眼部开合均值={avg_eye_opening:.4f}。"
    )


def build_video_context(video_path, sample_interval, sensevoice_model):
    face_summary = summarize_face_mesh(video_path, sample_interval)
    transcript = transcribe_with_sensevoice(video_path, sensevoice_model)

    blocks = [f"FaceMesh摘要：{face_summary}"]
    if transcript:
        blocks.append(f"SenseVoiceSmall转写：{transcript}")
    else:
        blocks.append("SenseVoiceSmall转写：未提取到有效语音，保留 FaceMesh 结果作为视频辅助信息。")
    return "\n".join(blocks)


