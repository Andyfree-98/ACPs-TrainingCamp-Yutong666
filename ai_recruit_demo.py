def sanitize_accel_env_for_llama_cpp():
    # llama-cpp on Windows will add CUDA/HIP dll dirs if env vars exist.
    # Remove stale paths to avoid FileNotFoundError during import.
    for key in ("CUDA_PATH", "HIP_PATH"):
        root = os.getenv(key, "").strip()
        if not root:
            continue

        root_path = Path(root)
        if not root_path.exists():
            os.environ.pop(key, None)
            continue

        if not (root_path / "bin").exists() and not (root_path / "lib").exists():
            os.environ.pop(key, None)


def normalize_score_value(value):
    try:
        numeric_value = float(value)
    except (TypeError, ValueError) as exc:
        raise ValueError(f"无效分数：{value}") from exc

    return round(max(0.0, min(10.0, numeric_value)), 1)

def calculate_weighted_total_score(parsed):
    total_score = 0.0
    for key, weight in SCORE_WEIGHTS.items():
        total_score += normalize_score_value(parsed[key]) * weight
    return round(total_score, 1)

def normalize_score_fields(parsed):
    for key in ("layer1_score", "layer2_score", "layer3_score"):
        parsed[key] = normalize_score_value(parsed[key])
    parsed["total_score"] = calculate_weighted_total_score(parsed)
    return parsed


@st.cache_resource(show_spinner=False)
def load_qwen_llm(model_path):
    if not model_path:
        raise RuntimeError("未配置 LOCAL_QWEN_GGUF_PATH。")

    model_file = Path(model_path)
    if not model_file.exists():
        raise FileNotFoundError(f"找不到本地模型文件：{model_file}")

    sanitize_accel_env_for_llama_cpp()

    try:
        from llama_cpp import Llama
    except ImportError as exc:
        raise RuntimeError("缺少 llama-cpp-python，请先安装依赖") from exc

    return Llama(
        model_path=str(model_file),
        n_ctx=LLAMA_N_CTX,
        n_gpu_layers=LLAMA_GPU_LAYERS,
        verbose=False,
    )


def analyze_candidate_with_qwen(resume_text, job_requirement, candidate_name, model_path, video_context=None):
    llm = load_qwen_llm(model_path)
    prompt = build_qwen_prompt(resume_text, job_requirement, candidate_name, video_context)

    try:
        result = llm.create_chat_completion(
            messages=[
                {"role": "system", "content": "你只输出合法JSON，不要输出多余文字。"},
                {"role": "user", "content": prompt},
            ],
            temperature=0.1,
            max_tokens=768,
        )
        content = result["choices"][0]["message"]["content"]
        parsed = parse_ai_json(content)
    except Exception as exc:
        st.error(f"本地 Qwen 调用失败：{exc}")
        return None

    required_keys = [
        "candidate_name",
        "layer1_score",
        "layer2_score",
        "layer3_score",
        "total_score",
        "level",
        "summary",
        "risk_points",
        "match_basis",
    ]
    if not all(key in parsed for key in required_keys):
        st.error(f"Qwen 返回 JSON 字段不完整：{parsed}")
        return None

    try:
        return normalize_score_fields(parsed)
    except ValueError as exc:
        st.error(f"Qwen 返回分数字段格式错误：{exc}")
        return None


