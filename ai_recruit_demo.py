def normalize_name_hint(value):
    if not value:
        return ""

    stem = Path(value).stem if Path(value).suffix else value
    stem = stem.lower()
    stem = re.sub(r"[\s_\-—–().【】\[\]{}<>]+", "", stem)
    stem = re.sub(r"(简历|resume|cv|视频|video|面试|interview|录屏|record|candidate|候选人|副本|final|draft)", "", stem)
    return stem


def extract_name_candidates(text):
    if not text:
        return []

    seen = []
    for token in re.findall(r"[\u4e00-\u9fff]{2,4}", text):
        if token not in seen and token not in {"简历", "岗位", "项目", "责任心", "学习能力", "团队协作"}:
            seen.append(token)
    for token in re.findall(r"[A-Za-z][A-Za-z0-9]{1,24}", text):
        if token not in seen:
            seen.append(token)
    return seen


def choose_candidate_name(resume_text, resume_filename, video_filename=None):
    resume_hints = extract_name_candidates(f"{resume_filename}\n{resume_text[:1200]}")
    video_hints = extract_name_candidates(video_filename or "")

    if video_hints:
        for resume_hint in resume_hints:
            resume_key = normalize_name_hint(resume_hint)
            for video_hint in video_hints:
                video_key = normalize_name_hint(video_hint)
                if resume_key and video_key and (
                    resume_key == video_key or resume_key in video_key or video_key in resume_key
                ):
                    return resume_hint, True

    if resume_hints:
        return resume_hints[0], bool(video_hints)

    return normalize_name_hint(resume_filename) or "未识别姓名", False


def match_video_for_resume(resume_filename, resume_text, video_files):
    resume_hints = [normalize_name_hint(item) for item in extract_name_candidates(f"{resume_filename}\n{resume_text[:1200]}")]
    resume_hints = [hint for hint in resume_hints if hint]

    best_match = None
    best_score = 0

    for video_file in video_files:
        video_hints = [normalize_name_hint(item) for item in extract_name_candidates(video_file.name)]
        video_hints = [hint for hint in video_hints if hint]

        score = 0
        for resume_hint in resume_hints:
            for video_hint in video_hints:
                if resume_hint == video_hint:
                    score = max(score, 3)
                elif resume_hint in video_hint or video_hint in resume_hint:
                    score = max(score, 2)
                elif resume_hint and video_hint and resume_hint[:2] == video_hint[:2]:
                    score = max(score, 1)

        if score > best_score:
            best_score = score
            best_match = video_file

    return best_match, best_score >= 2


